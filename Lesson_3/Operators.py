a = int(input('Введите число a: '))
b = int(input('Введите число b: '))

print(f'Сумма - {a+b}', f'Разность - {a-b}', f'Произведение - {a*b}', f'Возведение в степень - {a**b}', f'Деление по модулю - {a%b}', f'Целочисленное деление - {a//b}', sep='\n')
# % - делению по модулю (возвращает остаток), // - целочисленное деление (отбрасывает остаток)
