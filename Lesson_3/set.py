# set - неупорядоченная коллекция уникальных элементов

s = {1, 2, 3, 5, 3, 1, 4}         # создание множества. Способ1
print(s, type(s))

s1 = {'1', '2', '3', '5', '3', '1', '4'}
print(s1, type(s1))

s2 = set('Hello World')         # создание множества. Способ2
print(s2, type(s2))

s3 = set(range(5))         # создание множества. Способ3
print(s3, type(s3))

s4 = set()         # создание пустого множества
print(s4, type(s4))

l1 = [1, 4, 5, 6, 3, 1, 3]           # создание уникального списка
l2 = list(set(l1))
print(l2)

