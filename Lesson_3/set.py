s5 = {'4', '11', '1', 1}

print(s1 & s5)                # определение пересечений множеств
print(s1.intersection(s5))

print(s1 | s5)                 # объединение множеств
print(s1.union(s5))

print(s1.difference(s5))                  # вычитание множеств

